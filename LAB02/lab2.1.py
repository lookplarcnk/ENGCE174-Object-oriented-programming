# 1. Left half pyramid ปิรามิดครึ่งซ้าย
num_rows = int(input("Enter the number of rows : ")) # รับค่าจำนวนแถวจากผู้ใช้
for i in range(1, num_rows + 1): # loop สร้างรูปแบบสามเหลี่ยมโดย loop นอกสุดจะวน loop ตามจำนวนแถวที่ผู้ใช้ป้อนเข้ามา
    for j in range(i): # loop ภายในใช้สำหรับพิมพ์เครื่องหมายดอกจันทร์ (*) ในแต่ละแถว จำนวนครั้งที่วน loop จะเท่ากับ i ซึ่งเป็นลำดับของแถวที่กำลังทำงาน
        print("* ", end="") 
    print() # ขึ้นบรรทัดใหม่และเริ่มแถวใหม่

# 2. Inverted half pyramid ปิรามิดหัวกลับ
num_rows = int(input("Enter the number of rows : ")) # รับค่าจำนวนแถวจากผู้ใช้
for i in range(num_rows): # loop สร้างรูปแบบสามเหลี่ยมโดย loop นอกสุดจะวน loop ตามจำนวนแถวที่ผู้ใช้ป้อนเข้ามา
    for j in range(num_rows - i): # loop ภายในใช้สำหรับพิมพ์เครื่องหมายดอกจันทร์ (*) ในแต่ละแถว จำนวนครั้งที่วน loop จะเท่ากับ num_rows - i ซึ่งเป็นจำนวนของดอกจันทร์ที่จะพิมพ์ในแถวนั้นๆ ซึ่งจะลดลงเรื่อยๆ ตามการเพิ่มค่าของตัวแปร i ในการวน loop
        print("* ", end="")
    print() # ขึ้นบรรทัดใหม่และเริ่มแถวใหม่

# 3. Full pyramid ปิรามิดเต็ม
num_rows = int(input("Enter the number of rows : ")) # รับค่าจำนวนแถวจากผู้ใช้
for i in range(num_rows): # loop สร้างรูปแบบสามเหลี่ยมโดย loop นอกสุดจะวนลูปตามจำนวนแถวที่ผู้ใช้ป้อนเข้ามา 
    for j in range(num_rows - i - 1): # loop ภายในแรกใช้สำหรับพิมพ์ช่องว่าง (" ") ในแต่ละแถว โดยจำนวนครั้งที่วน loop จะเท่ากับ num_rows - i - 1 ซึ่งจะลดลงเรื่อยๆ ตามการเพิ่มค่าของตัวแปร i ในการวน loop
        print(" ", end="")
    for j in range(i + 1): # loop ภายในที่สองใช้สำหรับพิมพ์เครื่องหมายดอกจันทร์ (* ) ในแต่ละแถว จำนวนครั้งที่วน loop จะเท่ากับ i + 1 ซึ่งเป็นจำนวนของดอกจันทร์ที่จะพิมพ์ในแถวนั้นๆ เพิ่มขึ้นเรื่อยๆ ตามการเพิ่มค่าของตัวแปร i ในการวน loop
        print("* ", end="") 
    print() # ขึ้นบรรทัดใหม่และเริ่มแถวใหม่

