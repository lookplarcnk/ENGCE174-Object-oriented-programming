# Example 3: Sorting and reversing a list แสดงวิธีการจัดเรียงและสลับลำดับในลิสต์

numbers = [5, 2, 8, 1, 3]

# Sorting the list จัดเรียงลำดับของลิสต์
numbers.sort()
print("Sorted list:", numbers) # Output: [1, 2, 3, 5, 8] แสดงผล: [1, 2, 3, 5, 8]

# Reversing the list สลับลำดับของลิสต์
numbers.reverse()
print("Reversed list:", numbers) # Output: [8, 5, 3, 2, 1] แสดงผล: [8, 5, 3, 2, 1]